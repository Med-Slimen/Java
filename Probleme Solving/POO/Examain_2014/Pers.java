public class Pers {
    private int age;
    private int taille;
    private String nom;
    public Pers(int age, int taille, String nom) {
        this.age = age;
        this.taille = taille;
        this.nom = nom;
    }
    public int getAge() {
        return age;
    }
    public String getNom() {
        return nom;
    }
}
